function ham((ramp, Пөs, О¶s, P), t, Оұ=1)
    О”s = ramp(t)
    Ham = Оұ * (О”s[1] * P[0, 1] + О”s[2] * P[0, 2] + О”s[3] * P[0, 3] +
               Пөs[1] * P[0, 1] + (Пөs[2]) * P[2, 4] + (Пөs[3]) * P[3, 5] +
               -О”s[2] * О¶s[1] * О¶s[2] * P[1, 4] - О”s[3] * О¶s[1] * О¶s[3] * P[1, 5])
    return Ham
end

function ham_with_corrections((ramp, Пөs, О¶s, corr, P), t, Оұ=1)
    О”s = ramp(t)
    О”23 = вҲҡ(О”s[2]^2 + О”s[3]^2)
    О”31 = вҲҡ(О”s[3]^2 + О”s[1]^2)
    О”12 = вҲҡ(О”s[1]^2 + О”s[2]^2)
    Ham = Оұ * (О”s[1] * P[0, 1] + О”s[2] * P[0, 2] + О”s[3] * P[0, 3] +
               Пөs[1] * P[0, 1] + (Пөs[2] - corr * О¶s[1] * О¶s[3] * О”23 * О”s[3] / О”31) * P[2, 4] + (Пөs[3] - corr * О¶s[1] * О¶s[2] * О”23 * О”s[2] / О”12) * P[3, 5] +
               -О”s[2] * О¶s[1] * О¶s[2] * P[1, 4] - О”s[3] * О¶s[1] * О¶s[3] * P[1, 5])
    return Ham
end

function ham_with_corrections!(Ham, (ramp, Пөs, О¶s, corr, P), t, Оұ=1)
    О”s = ramp(t)
    О”23 = вҲҡ(О”s[2]^2 + О”s[3]^2)
    О”31 = вҲҡ(О”s[3]^2 + О”s[1]^2)
    О”12 = вҲҡ(О”s[1]^2 + О”s[2]^2)
    @. Ham = Оұ * (О”s[1] * P[0, 1] + О”s[2] * P[0, 2] + О”s[3] * P[0, 3] +
                  Пөs[1] * P[0, 1] + (Пөs[2] - corr * О¶s[1] * О¶s[3] * О”23 * О”s[3] / О”31) * P[2, 4] + (Пөs[3] - corr * О¶s[1] * О¶s[2] * О”23 * О”s[2] / О”12) * P[3, 5] +
                  #imag(О”s[2]) * (О¶s[1] * P[1, 2] + О¶s[2] * P[0, 4]) + imag(О”s[3]) * (О¶s[1] * P[1, 3] + О¶s[3] * P[0, 5]) +
                  -(О”s[2]) * О¶s[1] * О¶s[2] * P[1, 4] - (О”s[3]) * О¶s[1] * О¶s[3] * P[1, 5])
    return Ham
end
